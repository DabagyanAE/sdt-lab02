#include <iostream>

using namespace std;

// Программа является учебной /

int main ()
{
    int a, b;
    cout << "Enter A & B: \n\n";
    cin >> a >> b;
    cout << "A + B = " << a + b << '\n';
    cout << "2 * A = " << 2 * a << '\n';
    cout << "A - B = " << a - b << '\n';
    cout << "A * B = " << a * b << '\n';
    return 0;
}